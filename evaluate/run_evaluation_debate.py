# -*- coding: utf-8 -*-
import os
import sys
import argparse
import subprocess
import logging
import ast
import json
from run_evaluation_raw import normalize_entity_concept

# Set console encoding to UTF-8
try:
    sys.stdout.reconfigure(encoding='utf-8')
    sys.stderr.reconfigure(encoding='utf-8')
except AttributeError:
    pass

# Setup logging
logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')
logger = logging.getLogger("BioREDEvaluatorDebate")

# Define paths
project_root = r"E:\HK2(2025-2026)\Đồ Án 2\MainFolder\MyProject"
edc_main_dir = os.path.join(project_root, "edc-main")
evaluate_dir = os.path.join(project_root, "evaluate")

# Append edc-main to path for imports
sys.path.append(edc_main_dir)

# Load environment variables from .env in project root
env_path = os.path.join(project_root, ".env")
if os.path.exists(env_path):
    logger.info(f"Loading environment variables from {env_path}")
    with open(env_path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith("#"):
                continue
            if "=" in line:
                key, val = line.split("=", 1)
                os.environ[key.strip()] = val.strip().strip('"').strip("'")
    if "GROQ_API_KEY" in os.environ and "GROQ_KEY" not in os.environ:
        os.environ["GROQ_KEY"] = os.environ["GROQ_API_KEY"]

def main():
    parser = argparse.ArgumentParser(description="Run Multi-Agent Debate on extracted BioRED results and evaluate")
    parser.add_argument("--config", default=os.path.join(evaluate_dir, "config_debate.json"),
                        help="Path to JSON configuration file containing debate parameters")
    parser.add_argument("--input_json", default=None, 
                        help="Path to the result_at_each_stage.json generated by raw run")
    parser.add_argument("--output_dir", default=os.path.join(evaluate_dir, "outputs_debate"), 
                        help="Directory to save debated outputs")
    
    args = parser.parse_args()

    # Load JSON config
    config_data = {}
    if os.path.exists(args.config):
        logger.info(f"Loading configuration from {args.config}")
        try:
            with open(args.config, "r", encoding="utf-8") as f:
                config_data = json.load(f)
        except Exception as e:
            logger.error(f"Failed to parse config file: {e}")

    # Merge configuration
    oie_llm = config_data.get("oie_llm", "meta-llama/llama-3.3-70b-instruct")
    num_docs = config_data.get("num_docs", 9)
    output_dir = config_data.get("output_dir", args.output_dir)
    input_json_path = args.input_json or config_data.get("input_json_path", os.path.join(evaluate_dir, "outputs_raw", "result_at_each_stage.json"))

    if not os.path.isabs(output_dir):
        output_dir = os.path.abspath(os.path.join(project_root, output_dir))
    if not os.path.isabs(input_json_path):
        input_json_path = os.path.abspath(os.path.join(project_root, input_json_path))

    logger.info("="*60)
    logger.info("RUN CONFIGURATION (DEBATE & EVALUATION):")
    logger.info(f"  Debate Model:  {oie_llm}")
    logger.info(f"  Input JSON:    {input_json_path}")
    logger.info(f"  Num Docs:      {num_docs}")
    logger.info(f"  Output Dir:    {output_dir}")
    logger.info("="*60)

    if not os.path.exists(input_json_path):
        logger.error(f"Input JSON path {input_json_path} does not exist. Please run raw extraction first.")
        sys.exit(1)

    ref_txt_path = os.path.join(evaluate_dir, "biored_diabetes_references.txt")
    target_schema_path = os.path.join(evaluate_dir, "biored_schema.csv")

    if os.path.exists(output_dir):
        logger.info(f"Removing existing output directory: {output_dir}")
        import shutil
        shutil.rmtree(output_dir)
    os.makedirs(output_dir, exist_ok=True)

    # Filter references for subset if needed
    eval_ref_path = ref_txt_path
    if num_docs < 9:
        eval_ref_path = os.path.join(output_dir, "subset_references.txt")
        logger.info(f"Writing subset references to: {eval_ref_path}")
        with open(ref_txt_path, "r", encoding="utf-8") as f:
            ref_lines = f.readlines()
        with open(eval_ref_path, "w", encoding="utf-8") as f:
            f.writelines(ref_lines[:num_docs])

    # 1. Run Multi-Agent Debate
    logger.info("Running Multi-Agent Debate Gate...")
    debate_output_dir = os.path.join(output_dir, "debated_results")
    
    debate_cmd = [
        sys.executable,
        os.path.join(edc_main_dir, "debate_gate", "run_debate.py"),
        "--input_json_path", input_json_path,
        "--schema_path", target_schema_path,
        "--output_dir", debate_output_dir,
        "--debate_gate_model", oie_llm,
        "--debate_gate_fcs_threshold", "65.0",
    ]
    
    logger.info(f"Executing debate command: {' '.join(debate_cmd)}")
    subprocess.run(debate_cmd, check=True)
    
    debated_output_txt = os.path.join(debate_output_dir, "canon_kg_debated.txt")

    # 2. Normalization and Evaluation
    if os.path.exists(debated_output_txt):
        logger.info(f"Normalizing, filtering, and deduplicating debated triples in {debated_output_txt}...")
        with open(debated_output_txt, "r", encoding="utf-8") as f:
            lines = f.readlines()

        ref_lines_data = []
        if os.path.exists(eval_ref_path):
            with open(eval_ref_path, "r", encoding="utf-8") as f:
                for l in f:
                    if l.strip():
                        try:
                            ref_lines_data.append(ast.literal_eval(l.strip()))
                        except Exception as e:
                            logger.error(f"Failed to parse reference line: {e}")

        new_lines = []
        for line_idx, line in enumerate(lines):
            try:
                triplets = ast.literal_eval(line.strip())
                new_triplets = []
                seen = set()
                
                ref_entities = set()
                if line_idx < len(ref_lines_data):
                    for trip in ref_lines_data[line_idx]:
                        if len(trip) == 3:
                            ref_entities.add(trip[0].lower().strip())
                            ref_entities.add(trip[2].lower().strip())

                for trip in triplets:
                    if len(trip) != 3:
                        continue
                    
                    subj = trip[0].lower().strip()
                    pred = trip[1].strip()
                    obj  = trip[2].lower().strip()

                    invalid_entities = {'increase', 'decrease', 'elevated', 'reduced', 'inhibition', 'activation', 'elevation', 'excretion', 'treating'}
                    if subj in invalid_entities or obj in invalid_entities:
                        continue
                    
                    if len(subj) <= 2 and subj not in {'ca', 'na', 'k', 'k+'}:
                        continue
                    if len(obj) <= 2 and obj not in {'ca', 'na', 'k', 'k+'}:
                        continue

                    subj = normalize_entity_concept(subj, ref_entities)
                    obj  = normalize_entity_concept(obj, ref_entities)
                    if not subj or not obj:
                        continue

                    if line_idx < len(ref_lines_data):
                        for ref_trip in ref_lines_data[line_idx]:
                            if len(ref_trip) == 3:
                                ref_s = ref_trip[0].lower().strip()
                                ref_o = ref_trip[2].lower().strip()
                                if ref_s == obj and ref_o == subj:
                                    subj, obj = obj, subj
                                    break

                    diabetes_keywords = {
                        "diabetes", "diabetic", "insulin", "glucose", "glycemic", "glycaemic", 
                        "hba1c", "t2dm", "gdm", "ndi", "hyperglycemia", "hypoglycemia"
                    }
                    is_rel = False
                    for kw in diabetes_keywords:
                        if kw in subj or kw in obj:
                            is_rel = True
                            break
                    if not is_rel:
                        continue

                    t_tuple = (subj, pred, obj)
                    if t_tuple not in seen:
                        new_triplets.append([subj, pred, obj])
                        seen.add(t_tuple)

                new_lines.append(str(new_triplets) + "\n")
            except Exception as e:
                new_lines.append(line)

        normalized_pred_path = debated_output_txt.replace(".txt", "_normalized.txt")
        with open(normalized_pred_path, "w", encoding="utf-8") as f:
            f.writelines(new_lines)
        logger.info(f"Normalized debated triples saved to {normalized_pred_path}")

        # Run evaluation script
        logger.info("Running evaluation script (With Debate)...")
        eval_script_path = os.path.join(evaluate_dir, "evaluation_script_optimized.py")
        cmd = [
            sys.executable,
            eval_script_path,
            "--edc_output", normalized_pred_path,
            "--reference", eval_ref_path
        ]
        
        result = subprocess.run(cmd, capture_output=True, text=True, encoding="utf-8")
        
        print("\n" + "="*50)
        print("                 EVALUATION RESULTS (WITH DEBATE)")
        print("="*50)
        print(result.stdout)
        if result.stderr:
            print("Errors/Warnings:")
            print(result.stderr)
        print("="*50)

        # Save report
        report_path = os.path.join(output_dir, "evaluation_results.txt")
        with open(report_path, "w", encoding="utf-8") as f:
            f.write(result.stdout)
            if result.stderr:
                f.write("\n\nErrors/Warnings:\n")
                f.write(result.stderr)
        logger.info(f"Saved debated evaluation report to: {report_path}")

if __name__ == "__main__":
    main()
